    private void configurarData() {
        date_field.setText("yyyy-mm-dd");
        date_field.setForeground(Color.GRAY);

        date_field.addFocusListener(new java.awt.event.FocusAdapter() {
            @Override
            public void focusGained(java.awt.event.FocusEvent evt) {
                if (date_field.getText().equals("yyyy-mm-dd")) {
                    date_field.setText("");
                    date_field.setForeground(Color.BLACK);
                }
            }

            @Override
            public void focusLost(java.awt.event.FocusEvent evt) {
                if (date_field.getText().isEmpty()) {
                    date_field.setText("yyyy-mm-dd");
                    date_field.setForeground(Color.GRAY);
                }
            }
        });
    }
    
    private void addNF() {
        NF newNF = new NF();

        // Only parse ID if not blank
        String idText = id_field.getText().trim();
        if (!idText.isEmpty()) {
            try {
                newNF.setId(Integer.parseInt(idText));
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "ID inv치lido.");
                return;
            }
        }

        newNF.setDate(date_field.getText());

        try {
            newNF.settValue(Double.parseDouble(tValue_field.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Valor total inv치lido.");
            return;
        }

        String clientIdText = clientId_field.getText().trim();

        if (costumer_option.isSelected()) {
            if (!clientIdText.isEmpty()) {
                newNF.setCustomerID(Integer.parseInt(clientIdText));
                newNF.setSuplierID(null);
            } else {
                JOptionPane.showMessageDialog(this, "ID do Cliente est치 vazio.");
                return;
            }
        } else if (suplier_option.isSelected()) {
            if (!clientIdText.isEmpty()) {
                newNF.setCustomerID(null);
                newNF.setSuplierID(Integer.parseInt(clientIdText));
            } else {
                JOptionPane.showMessageDialog(this, "ID do Fornecedor est치 vazio.");
                return;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, selecione Cliente ou Fornecedor.");
            return;
        }

        NFDAO sDao = new NFDAO();
        sDao.add(newNF);

        clearForm();
        System.out.println("addNF executado com sucesso");
    }

    
    private void updateNF (){
        NF newNF = new NF();

        newNF.setId(Integer.parseInt(id_field.getText()));
        newNF.setDate(date_field.getText());
        newNF.settValue(Double.parseDouble(tValue_field.getText()));
        if(costumer_option.isSelected()){;
            newNF.setCustomerID(Integer.parseInt(clientId_field.getText()));
            newNF.setSuplierID(0);
        }else{
            newNF.setCustomerID(0);
            newNF.setSuplierID(Integer.parseInt(clientId_field.getText()));
        }

        NFDAO cDAO = new NFDAO();
        cDAO.update(newNF);

        clearForm();

        System.out.println("editCustomer escolhida");
    }
    
